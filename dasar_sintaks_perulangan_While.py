"""
Perulangan Sederhana Menngunaka While
"""

Jumlah_buku =10
print('ibu berkata, baca semua bukumu')

jumlah_buku_yang_sudahdibaca = 0

while jumlah_buku_yang_sudahdibaca < Jumlah_buku:
    jumlah_buku_yang_sudahdibaca = jumlah_buku_yang_sudahdibaca + 1
    print(f"buku ke {jumlah_buku_yang_sudahdibaca} sudah di baca")


print(f"jumlah buku yang sudah di baca {jumlah_buku_yang_sudahdibaca}")